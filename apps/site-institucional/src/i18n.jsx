const LANGS = ["PT", "EN", "ES"];
const KEY = { PT: "pt", EN: "en", ES: "es" };

const LangContext = createContext({ lang: "PT", setLang: () => {} });

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState(() => {
    if (typeof localStorage !== "undefined") {
      const saved = localStorage.getItem("oc-lang");
      if (saved && LANGS.includes(saved)) return saved;
    }
    return "PT";
  });

  useEffect(() => {
    if (typeof localStorage !== "undefined") localStorage.setItem("oc-lang", lang);
    if (typeof document !== "undefined") {
      document.documentElement.lang = { PT: "pt-BR", EN: "en", ES: "es" }[lang];
    }
  }, [lang]);

  return <LangContext.Provider value={{ lang, setLang, langs: LANGS }}>{children}</LangContext.Provider>;
}

export function useLang() {
  return useContext(LangContext);
}

/** Retorna a árvore de textos do idioma atual. */
export function useT() {
  const { lang } = useContext(LangContext);
  return STRINGS[KEY[lang]] ?? STRINGS.pt;
}
